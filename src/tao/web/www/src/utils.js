const isValidIPv4 = ip => ipv4.test(ip)

const isNotEmpty = s => s !== '' && s !== null && s !== undefined


const getSearchParams = (s) => {
  let params = {}
  let search = new URLSearchParams(s)
  for (let p of search) {
    params[p[0]] = p[1]
  }

  return params
}

